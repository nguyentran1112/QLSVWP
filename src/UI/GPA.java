    private void loadgpa(){
        try { 
             GPAdao dao = new GPAdao();
            ArrayList<GPAclass> list = dao.findtopby(1);
            tbModel1.setRowCount(0);
            for (GPAclass g :list){
                tbModel1.addRow(new Object[]{g.getMssv(),g.getOOP(),g.getImageP(),g.getPSD(),g.getSOC(),
                ( g.getOOP() + g.getImageP() + g.getPSD() + g.getSOC() )/4
            
            });
            }
            tbModel1.fireTableDataChanged();
            
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialog.showErrorDialog(jPanel1, e.getMessage(), "Lỗi");
        }
    
    }

