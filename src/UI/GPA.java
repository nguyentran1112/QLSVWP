    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        try {
            if(txtmssv1.getText().equals("")){
                 MessageDialog.showMessageDialog(jPanel1, "Nhập MSSV","Lỗi");
                 return;
            }
            GPAdao dao = new GPAdao();
            GPAclass gp = dao.findby(txtmssv1.getText());
            if(gp == null){
                MessageDialog.showMessageDialog(jPanel1, "SV không tồn tại","Lỗi");
                return;
            }
            if(MessageDialog.showConfirmDialog(jPanel1, "Bạn có muốn xoá thông tin không?","Hỏi")==JOptionPane.NO_OPTION){
                return;
            }
            if(dao.deleteby(txtmssv1.getText())){
                MessageDialog.showMessageDialog(jPanel1, "Điểm của SV đã được xoá","Thông báo");
                
            }
            else{
                MessageDialog.showMessageDialog(jPanel1, "Điểm của sinh viên không xoá được","Lỗi");
            
            }
            
            loadgpa();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialog.showMessageDialog(jPanel1, e.getMessage(),"Lỗi");
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void tbgpaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbgpaMouseClicked
        try {
            int row = tbgpa.getSelectedRow();
            if(row>=0){
                String mssv =(String) tbgpa.getValueAt(row, 0);
                GPAdao dao = new GPAdao();
                GPAclass gp = dao.findby(mssv);
                if(gp != null){
                    txtmssv1.setText(gp.getMssv());
                    //
                    txtoop.setText(Float.toString(gp.getOOP()));
                    txtpsd.setText(Float.toString(gp.getPSD()));
                    txtim.setText(Float.toString(gp.getImageP()));
                    txtsoc.setText(Float.toString(gp.getSOC()));
                    
                
                
                
                }
            }
            txtmssv1FocusLost(null);
            txtoopFocusLost(null);
           
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialog.showErrorDialog(this, e.getMessage(),"Lỗi");
        }
    }//GEN-LAST:event_tbgpaMouseClicked
    

