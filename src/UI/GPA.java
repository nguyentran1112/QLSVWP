    private void txtoopFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtoopFocusLost
        if ((txtoop.getText().equals(""))||(txtpsd.getText().equals(""))||(txtim.getText().equals(""))||(txtsoc.getText().equals(""))){
            return;
        }
               
        float oop = Float.parseFloat(txtoop.getText());
        float psd = Float.parseFloat(txtpsd.getText());
        float im = Float.parseFloat(txtim.getText());
        float soc = Float.parseFloat(txtsoc.getText());
        
        float avg = (oop + psd + im + soc)/4;
        String st;
        st = String.format("%.2f", avg);
        lbgpa.setText(st);
    }//GEN-LAST:event_txtoopFocusLost

