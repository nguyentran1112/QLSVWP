    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
       StringBuilder sb = new StringBuilder();
       DataValidator.validateEmty(txtmssv1, sb, "Nhập mssv");
       DataValidator.validateEmty(txtsoc, sb, "Nhập điểm môn soc");
       if(sb.length()>0){
           MessageDialog.showErrorDialog(jPanel2, sb.toString(), "Lỗi");
           return;
       }
        try {
            GPAclass gp = new GPAclass();
            gp.setMssv(txtmssv1.getText());
            gp.setOOP(Float.parseFloat(txtoop.getText()));
            gp.setImageP(Float.parseFloat(txtim.getText()));
            gp.setPSD(Float.parseFloat(txtpsd.getText()));
            gp.setSOC(Float.parseFloat(txtsoc.getText()));
            GPAdao dao = new GPAdao();
            if(dao.findby(txtmssv1.getText()) !=null){
                if (MessageDialog.showConfirmDialog(jPanel2,"Bạn có muốn cập nhật lại điểm không","Hỏi")==JOptionPane.NO_OPTION){
                    return;
                }
                if(dao.update(gp)){
                MessageDialog.showMessageDialog(jPanel2, "Đã cập nhật","Thông báo");
            }
                else{
                 MessageDialog.showMessageDialog(jPanel2, "Không cập nhật được","Lỗi");
            }}
            else{
                if(dao.insert(gp)){
                    MessageDialog.showMessageDialog(jPanel2, "Đã lưu","Thông báo");
                }
                else{
                     MessageDialog.showMessageDialog(jPanel2, "Không lưu được","Lỗi");
                }
            }
            loadgpa();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialog.showErrorDialog(jPanel2, e.getMessage(),"Lỗi");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

