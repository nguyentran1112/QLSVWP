    private void tablestudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablestudentMouseClicked
        try {
            int row = tablestudent.getSelectedRow();
            if(row>=0){
                String mssv =(String) tablestudent.getValueAt(row, 0);
                Studentdao dao = new Studentdao();
                People.SV sv = dao.findby(mssv);
                if(sv != null){
                    txtmssv1.setText(sv.getMssv());
                    txtname.setText(sv.getName());
                    txtaddress.setText(sv.getaddress());
                    txtphone.setText(Integer.toString(sv.getphone()));
                    txtid.setText(Integer.toString(sv.getid()));
                    txtdate.setText(new SimpleDateFormat("dd-MM-yyyy").format(sv.getbirthDate())); //convert String to date.
                    txtsex.setSelectedItem(sv.getsex());
                
                
                
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialog.showErrorDialog(parentFrom, e.getMessage(),"Lỗi");
        }
    }//GEN-LAST:event_tablestudentMouseClicked

