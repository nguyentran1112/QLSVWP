    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        StringBuilder sb = new StringBuilder();
        
        DataValidator.validateEmty(txtname, sb, "Hãy nhập tên sinh viên");
        if(sb.length()>0){
            MessageDialog.showErrorDialog(parentFrom, sb.toString(),"Lỗi");
            return;
        
        }
        if(MessageDialog.showConfirmDialog(parentFrom, "Bạn có muốn cập nhật không","Hỏi")== JOptionPane.NO_OPTION){
            return;
        }
        
        try {
            People s = new  People();
            SV sv = s.new SV();
            sv.setMssv(txtmssv1.getText());
            sv.setName(txtname.getText());
            sv.setaddress(txtaddress.getText());
            sv.setid(Integer.parseInt(txtid.getText()));
            sv.setphone(Integer.parseInt(txtphone.getText()));
            sv.setsex((String) txtsex.getSelectedItem());
            
            try {
            sv.setbirthDate(new SimpleDateFormat("dd-MM-yyyy").parse(txtdate.getText()));
         
                }
          
            catch(ParseException ex){
                MessageDialog.showErrorDialog(parentFrom,ex.getMessage(),"You type wrong format date/n Bạn đã nhập sai định dạng ngày tháng");
                ex.printStackTrace();
                                    }
           Studentdao dao = new Studentdao();
            if(dao.update(sv)){
                MessageDialog.showMessageDialog(parentFrom, "Thông tin đã được cập nhật","Thông báo");
                loadData();
            }
            else{
                MessageDialog.showMessageDialog(parentFrom, "Thông tin chưa được cập nhật","Lỗi");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialog.showErrorDialog(parentFrom, e.getMessage(),"Error");
        }
    }//GEN-LAST:event_btnupdateActionPerformed

